public class JobMixProbability {

	private int A;
	private int B;
	private int C;

	public JobMixProbability(int A, int B, int C) {
		this.A = A;
		this.B = B;
		this.C = C;
	}

	public int getA() {
		return this.A;
	}

	public int getB() {
		return this.B;
	}

	public int getC() {
		return this.C;
	}

}
