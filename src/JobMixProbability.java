public class JobMixProbability {

	private double A;
	private double B;
	private double C;

	public JobMixProbability(double A, double B, double C) {
		this.A = A;
		this.B = B;
		this.C = C;
	}

	public double getA() {
		return this.A;
	}

	public double getB() {
		return this.B;
	}

	public double getC() {
		return this.C;
	}

}
